    private void jbtLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtLoginActionPerformed
       int count = 0;
       
       
        
        if (jtxCPF.getText().equals("") || jpwSenha.getText().equals("")) {
            //Preencha todos os campos
            JOptionPane.showMessageDialog(null, "Preencha todos os campos dispon√≠veis para continuar!", "Aviso!", JOptionPane.WARNING_MESSAGE);
        } else if (jtxCPF.getText().equals("0") && jpwSenha.getText().equals("admin")) {
            new Principal().setVisible(true);
        } else {
            for (int i = 0; i < bd.getFuncionarios().size(); i++) {
                
                if (bd.getFuncionarios().get(i).getSenha().equals(jpwSenha.getText()) && bd.getFuncionarios().get(i).getCpf().equals(jtxCPF.getText())) {
                    if (bd.getFuncionarios().get(i) instanceof Ator) {
                        new VisualizarAtor((Ator) bd.getFuncionarios().get(i)).setVisible(true);
                        dispose();
                        break;

                    } else if (bd.getFuncionarios().get(i) instanceof Diretor) {
                        new VisualizarDiretor((Diretor) bd.getFuncionarios().get(i)).setVisible(true);
                        dispose();
                        break;
                    }
                }
                

            }
            
            

        }
    }//GEN-LAST:event_jbtLoginActionPerformed

