    private void jtbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jtbSalvarActionPerformed
        //ATOR
        if(jrbAtor.isSelected()){
            if (jtxNome.getText().equals("") || jtxCpf.getText().equals("") 
                    || jtxTelefone.getText().equals("") || jtxEmail.getText().equals("") 
                    || jtxSexo.getText().equals("") || jtxData_nascimento.getText().equals("") 
                    || jtxNacionalidade.getText().equals("") 
                    || jtxBiografia.getText().equals("") || jtxExperiencia.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Preencha todos os campos disponíveis para continuar!", "Aviso!", JOptionPane.WARNING_MESSAGE);
            } else if(cpfValido(jtxCpf.getText()) == false){
                JOptionPane.showMessageDialog(null, "Cpf inválido!", "Aviso!", JOptionPane.WARNING_MESSAGE);   
            } else{
                Date date;
                SimpleDateFormat DateFor = new SimpleDateFormat("dd/MM/yyyy");
                try{
                    Date dates = DateFor.parse(jtxData_nascimento.getText());
                    Ator novoAtor = new Ator(Integer.parseInt(jtxExperiencia.getText()),jtxBiografia.getText(),jtxCpf.getText(),
                        jtxNome.getText(), jpwSenha.getText(),jtxEmail.getText(),jtxTelefone.getText(),dates,jtxSexo.getText(),
                        jtxNacionalidade.getText());
                    bd.getFuncionarios().add(novoAtor);
                    BancoDeDados.writeBancoDeDados(bd);
                    
                    JOptionPane.showMessageDialog(null, "Ator Cadastrado com sucesso!", "Aviso!", JOptionPane.WARNING_MESSAGE);
                         
                }catch (ParseException e) {e.printStackTrace();}

            }
        } 
        //DIRETOR
        else if(jrbDiretor.isSelected()){
            if (jtxNome.getText().equals("") | jtxCpf.getText().equals("") 
                    | jtxTelefone.getText().equals("") | jtxEmail.getText().equals("") 
                    | jtxSexo.getText().equals("") | jtxData_nascimento.getText().equals("") 
                    | jtxNacionalidade.getText().equals("") |  jtxPercentual.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Preencha todos os campos disponíveis para continuar!", "Aviso!", JOptionPane.WARNING_MESSAGE);
            } else if(cpfValido(jtxCpf.getText()) == false){
                JOptionPane.showMessageDialog(null, "Cpf inválido!", "Aviso!", JOptionPane.WARNING_MESSAGE);   
            } else{
                Date date;
                SimpleDateFormat DateFor = new SimpleDateFormat("dd/MM/yyyy");
                try{
                    Date dates = DateFor.parse(jtxData_nascimento.getText());
                    Diretor novoDiretor = new Diretor(Double.parseDouble(jtxPercentual.getText()),jtxCpf.getText(),
                        jtxNome.getText(),jpwSenha.getText(),jtxEmail.getText(),jtxTelefone.getText(),dates,jtxSexo.getText(),
                        jtxNacionalidade.getText());
                    bd.getFuncionarios().add(novoDiretor);
                    BancoDeDados.writeBancoDeDados(bd);
                    
                    JOptionPane.showMessageDialog(null, "Diretor Cadastrado com sucesso!", "Aviso!", JOptionPane.WARNING_MESSAGE);
                         
                }catch (ParseException e) {e.printStackTrace();}
                
            }
        }
        
        //carregar os dados da tabela
        carregarTabelaFuncionario();
        
        //limpar os campos
        jtxNome.setText("");
        jtxCpf.setText("");
        jpwSenha.setText("");
        jtxTelefone.setText("");
        jtxEmail.setText("");
        jtxData_nascimento.setText("");
        jtxSexo.setText("");
        jtxNacionalidade.setText("");
        
        jtxPercentual.setText("");
        jtxExperiencia.setText("");
        jtxBiografia.setText("");
    }//GEN-LAST:event_jtbSalvarActionPerformed

