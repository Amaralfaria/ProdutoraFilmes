    private void tblFuncionarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblFuncionarioMouseClicked
        int i = tblFuncionario.getSelectedRow();
        
        if(i>=0 && i<bd.getFuncionarios().size()){
            Funcionario func = bd.getFuncionarios().get(i);
            
            SimpleDateFormat formatter = new SimpleDateFormat("MM/dd/yyyy");  
            String strDate = formatter.format(func.getData_nascimento());
            
            jtxNome.setText(func.getNome());
            jtxCpf.setText(func.getCpf());
            jtxTelefone.setText(func.getTelefone());
            jtxEmail.setText(func.getEmail());
            jtxData_nascimento.setText(strDate);
            jtxSexo.setText(func.getSexo());
            jtxNacionalidade.setText(func.getNacionalidade());
            
        }
        
        jlbCpf.setEnabled(false);
        jlbData_nascimento.setEnabled(false);
        
        jtxCpf.setEnabled(false);
        jtxData_nascimento.setEnabled(false);
        
        jtbEditar.setEnabled(true);
    }//GEN-LAST:event_tblFuncionarioMouseClicked

    private void jtbEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jtbEditarActionPerformed
        int index = tblFuncionario.getSelectedRow();
        
        bd.getFuncionarios().get(index).setNome(jtxNome.getText());
        bd.getFuncionarios().get(index).setSenha(jpwSenha.getText());
        bd.getFuncionarios().get(index).setTelefone(jtxTelefone.getText());
        bd.getFuncionarios().get(index).setEmail(jtxEmail.getText());
        bd.getFuncionarios().get(index).setSexo(jtxSexo.getText());
        bd.getFuncionarios().get(index).setNacionalidade(jtxNacionalidade.getText());
        
        
        JOptionPane.showMessageDialog(null, "Dados editados com sucesso!", "Aviso!", JOptionPane.WARNING_MESSAGE);
        
        //salvar no banco de dados
        BancoDeDados.writeBancoDeDados(bd);
     
        //carregar os dados da tabela
        carregarTabelaFuncionario();
        
        jlbCpf.setEnabled(true);
        jlbData_nascimento.setEnabled(true);
        
        jtxCpf.setEnabled(true);
        jtxData_nascimento.setEnabled(true);

        //limpar os campos
        jtxNome.setText("");
        jtxCpf.setText("");
        jpwSenha.setText("");
        jtxTelefone.setText("");
        jtxEmail.setText("");
        jtxData_nascimento.setText("");
        jtxSexo.setText("");
        jtxNacionalidade.setText("");
        
        jtxPercentual.setText("");
        jtxExperiencia.setText("");
        jtxBiografia.setText("");
    }//GEN-LAST:event_jtbEditarActionPerformed

